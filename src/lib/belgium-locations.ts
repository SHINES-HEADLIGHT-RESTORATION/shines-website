export type BelgiumCity = {
  name: string;
  slug: string;
};

export type BelgiumProvince = {
  id: string;
  name: string;
  nameNl: string;
  region: "Flanders" | "Wallonia" | "Brussels";
  cities: BelgiumCity[];
};

function city(name: string, slug?: string): BelgiumCity {
  return {
    name,
    slug: slug ?? name.toLowerCase().replace(/\s+/g, "-").replace(/'/g, ""),
  };
}

