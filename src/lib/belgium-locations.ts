export const allBelgiumCities = belgiumProvinces.flatMap((province) =>
  province.cities.map((c) => ({ ...c, province })),
);

export function getCityBySlug(slug: string) {
  return allBelgiumCities.find((entry) => entry.slug === slug) ?? null;
}

export const locationsPagePath = "/locations" as const;

export function cityLocationPath(slug: string): string {
  return `${locationsPagePath}/${slug}`;
}
